<?php
// Example PHP code for solving mathematical equations

$x = 5;
$y = 2;

// Addition
$result_addition = $x + $y;

// Subtraction
$result_subtraction = $x - $y;

// Multiplication
$result_multiplication = $x * $y;

// Division
$result_division = $x / $y;

// Modulo
$result_modulo = $x % $y;

// Exponentiation
$result_exponentiation = pow($x, $y);

// Square root
$result_square_root = sqrt($x);

// Factorial
$result_factorial = factorial($x);
